# ----------------------------------------------------------------------
# Функции для обеспечения существования директорий
# ----------------------------------------------------------------------
def ensure_directories() -> None:
    """Создаёт все необходимые директории (models, logs, output), если их нет."""
    for dir_path in [MODELS_DIR, LOGS_DIR, OUTPUT_DIR]:
        if not os.path.exists(dir_path):
            os.makedirs(dir_path)
            print(f"Создана директория: {dir_path}")

